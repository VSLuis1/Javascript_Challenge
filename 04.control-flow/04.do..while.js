// While loop

// Example 1
let i = 0;
while (i <= 5) {
    console.log(i);
    i++;
    }

// Array
const colors = ['red', 'green', 'blue'];
let j = 0; // j is the index of the array
while (j < colors.length) {
    console.log(colors[j]);
    j++;
    }

// do..while loop
let score = 0;
do {
    console.log(score);
    score++;
    }  while (score <= 10);






