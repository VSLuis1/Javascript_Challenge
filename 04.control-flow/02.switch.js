// Switch statement

// Example
let month = "march";
switch (month) {
    case "january":
        console.log("January");
        break;
    case "february":
        console.log("February");
        break;
    case "march":
        console.log("March");
        break;
    case "april":
        console.log("April");
        break;

    default:
        console.log("Month not found");
        break;
}


